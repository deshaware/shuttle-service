package com.deshaware.shuttleservice.helper;


public class Coordinates {
    public double lat;
    public double lon; 
    
    public void setLat(double lat) {
        this.lat = lat;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}